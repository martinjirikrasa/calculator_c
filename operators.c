#include "my_lib.h"

int	plus(int num1, int num2)
{
	return (num1 + num2);
}

int	minus(int num1, int num2)
{
	return (num1 - num2);
}

int	times(int num1, int num2)
{
	return (num1 * num2);
}

int	divide(int num1, int num2)
{
	return (num1 / num2);
}

int	mod(int num1, int num2)
{
	return (num1 % num2);
}
